// progress items (per zone: 2 大項 / 2 中項 / 4 細項) with leaf assignments that
// make zone visibility self-verifying. Idempotent (on conflict / where not
// exists). See .planning/TEST-PROGRAM.md. Run: node supabase/seed-test-rig.js
// =============================================================
const fs = require('fs')

const PW = 'CKtest2026'
const ADMINPW = 'CKtestADMIN2026!'
const PROJ = 'bbbb0000-0000-0000-0000-000000000001'
const uid = (phone) => `bbbb0000-0000-0000-0000-0000${phone}`
const ADMIN = uid('62000099')
const PM = uid('62000001')

const ZONES = [
  { idx: 1, zid: 'z-1', name: '一座', L: 'A' },
  { idx: 2, zid: 'z-2', name: '二座', L: 'B' },
  { idx: 3, zid: 'z-3', name: '三座', L: 'C' },
  { idx: 4, zid: 'z-ext', name: '外圍', L: 'D' },
]

// ---- accounts ----
const accounts = [
  { phone: '62000099', name: '測試管理員', grole: 'admin', srole: null, pw: ADMINPW, mem: null },
  { phone: '62000001', name: '測 PM', grole: 'pm', srole: null, pw: PW, mem: 'pm' },
  { phone: '62000002', name: '測 老總', grole: 'general_foreman', srole: null, pw: PW, mem: 'general_foreman' },
  { phone: '62000003', name: '測 業主', grole: 'owner', srole: null, pw: PW, mem: 'owner' },
  { phone: '62000004', name: '測 安全主任', grole: 'safety_officer', srole: 'safety', pw: PW, mem: 'safety_officer' },
]
for (const z of ZONES) {
  accounts.push({ phone: `620${z.idx}0001`, name: `${z.name}工程師`, grole: 'main_contractor', srole: 'engineer', pw: PW, mem: 'main_contractor' })
  accounts.push({ phone: `620${z.idx}0002`, name: `${z.name}管工`, grole: 'main_contractor', srole: 'foreman', pw: PW, mem: 'main_contractor' })
  accounts.push({ phone: `620${z.idx}0003`, name: `${z.name}判頭`, grole: 'subcontractor', srole: null, pw: PW, mem: 'subcontractor' })
  accounts.push({ phone: `620${z.idx}0004`, name: `${z.name}工人`, grole: 'subcontractor_worker', srole: null, pw: PW, mem: 'subcontractor_worker' })
}

const q = (s) => s === null || s === undefined ? 'null' : `'${String(s).replace(/'/g, "''")}'`
const acctRow = (a) => `(${q(uid(a.phone))}::uuid, ${q(a.phone)}, ${q(a.name)}, ${q(a.grole)}, ${q(a.srole)}::text, ${q(a.pw)})`
const acctCTE = `with acct(uid, phone, nm, grole, srole, pw) as (values\n  ${accounts.map(acctRow).join(',\n  ')}\n)`

let sql = `-- AUTO-GENERATED by supabase/seed-test-rig.js — do not hand-edit.
-- [TEST] permission test rig. Idempotent. Apply once.
create extension if not exists pgcrypto with schema extensions;

-- 1) auth.users
${acctCTE}
insert into auth.users (id, instance_id, aud, role, email, encrypted_password, email_confirmed_at,
  raw_app_meta_data, raw_user_meta_data, created_at, updated_at,
  confirmation_token, recovery_token, email_change, email_change_token_new, email_change_token_current,
  phone_change, phone_change_token, reauthentication_token)
select a.uid, '00000000-0000-0000-0000-000000000000'::uuid, 'authenticated', 'authenticated',
  a.phone || '@phone.local', extensions.crypt(a.pw, extensions.gen_salt('bf')), now(),
  '{"provider":"email","providers":["email"]}'::jsonb, '{}'::jsonb, now(), now(),
  '', '', '', '', '', '', '', ''
from acct a
on conflict (id) do nothing;

-- 2) auth.identities
${acctCTE}
insert into auth.identities (id, user_id, provider_id, provider, identity_data, last_sign_in_at, created_at, updated_at)
select gen_random_uuid(), a.uid, a.uid::text, 'email',
  jsonb_build_object('sub', a.uid::text, 'email', a.phone || '@phone.local', 'email_verified', true),
  now(), now(), now()
from acct a
where not exists (select 1 from auth.identities i where i.user_id = a.uid);

-- 3) user_profiles
${acctCTE}
insert into user_profiles (id, phone, name, global_role, sub_role)
select a.uid, a.phone, a.nm, a.grole, a.srole from acct a
on conflict (id) do nothing;

-- 4) project [TEST] 測試大廈項目 (4 zones)
insert into projects (id, name, zones, assigned_pm_ids, created_by, project_type, ai_enabled)
values (${q(PROJ)}::uuid, '[TEST] 測試大廈項目',
  '[{"id":"z-1","name":"一座"},{"id":"z-2","name":"二座"},{"id":"z-3","name":"三座"},{"id":"z-ext","name":"外圍"}]'::jsonb,
  array[${q(PM)}]::uuid[], ${q(ADMIN)}::uuid, 'general', true)
on conflict (id) do nothing;

-- 5) project_members (20 approved; admin is global, not a member)
`

const memRows = accounts.filter(a => a.mem).map(a => `(${q(uid(a.phone))}::uuid, ${q(a.mem)})`)
sql += `with mem(uid, mrole) as (values\n  ${memRows.join(',\n  ')}\n)
insert into project_members (user_id, project_id, role, status, applied_at, approved_by, approved_at)
select m.uid, ${q(PROJ)}::uuid, m.mrole, 'approved', now(), ${q(ADMIN)}::uuid, now()
from mem m
where not exists (select 1 from project_members pm where pm.user_id = m.uid and pm.project_id = ${q(PROJ)}::uuid);

-- 6) progress_items (per zone: 2 大項 / 2 中項 / 4 細項; leaf assignments self-document expected viewers)
`

// build 32 items
let seq = 0
const itemId = () => { seq++; return `bbbb1111-0000-0000-0000-${String(seq).padStart(12, '0')}` }
const items = []
for (const z of ZONES) {
  const eng = uid(`620${z.idx}0001`), foreman = uid(`620${z.idx}0002`), judou = uid(`620${z.idx}0003`), worker = uid(`620${z.idx}0004`)
  const root1 = itemId()
  items.push({ id: root1, parent: null, code: `${z.L}1`, title: `${z.name}-上層結構〔大項〕`, level: 1, zid: z.zid, assign: [] })
  const mid1 = itemId()
  items.push({ id: mid1, parent: root1, code: `${z.L}1.1`, title: `${z.name}-結構-3樓〔中項〕`, level: 2, zid: z.zid, assign: [] })
  items.push({ id: itemId(), parent: mid1, code: `${z.L}1.1.1`, title: `${z.name}-3樓柱〔細項·應見:${z.name}工程師+管工〕`, level: 3, zid: z.zid, assign: [eng, foreman] })
  items.push({ id: itemId(), parent: mid1, code: `${z.L}1.1.2`, title: `${z.name}-3樓樓板〔細項·應見:${z.name}判頭+工人〕`, level: 3, zid: z.zid, assign: [judou, worker] })
  const root2 = itemId()
  items.push({ id: root2, parent: null, code: `${z.L}2`, title: `${z.name}-機電〔大項〕`, level: 1, zid: z.zid, assign: [] })
  const mid2 = itemId()
  items.push({ id: mid2, parent: root2, code: `${z.L}2.1`, title: `${z.name}-機電-喉電〔中項〕`, level: 2, zid: z.zid, assign: [] })
  items.push({ id: itemId(), parent: mid2, code: `${z.L}2.1.1`, title: `${z.name}-喉管〔細項·應見:${z.name}判頭〕`, level: 3, zid: z.zid, assign: [judou] })
  items.push({ id: itemId(), parent: mid2, code: `${z.L}2.1.2`, title: `${z.name}-電線〔細項·應見:${z.name}工程師〕`, level: 3, zid: z.zid, assign: [eng] })
}
const itemRow = (it) => {
  const parent = it.parent ? `${q(it.parent)}::uuid` : 'null'
  const assign = it.assign.length ? `array[${it.assign.map(u => q(u)).join(',')}]::uuid[]` : `'{}'::uuid[]`
  return `(${q(it.id)}::uuid, ${q(PROJ)}::uuid, ${parent}, ${q(it.code)}, ${q(it.title)}, ${q(it.zid)}, ${it.level}, 'percentage', 'not-started', ${assign}, '{}'::uuid[])`
}
sql += `insert into progress_items (id, project_id, parent_id, code, title, zone_id, level, tracking_mode, status, assigned_to, delegated_to)
values
  ${items.map(itemRow).join(',\n  ')}
on conflict (id) do nothing;
`

fs.writeFileSync(__dirname + '/seed-test-rig.sql', sql, 'utf8')
console.log('wrote seed-test-rig.sql:', sql.length, 'bytes |', accounts.length, 'accounts |', items.length, 'items')
